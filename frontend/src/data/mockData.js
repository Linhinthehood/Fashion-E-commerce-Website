// Helper functions để lọc sản phẩm
export const getFeaturedProducts = () => products.filter(product => product.featured);
export const getNewProducts = () => products.slice(0, 6);
export const getSaleProducts = () => products.filter(product => product.originalPrice > product.price);
export const getProductsByGender = (gender) => products.filter(product => product.gender === gender || product.gender === 'Unisex');
export const getProductsByCategory = (category) => products.filter(product => 
  product.masterCategory === category || 
  product.subCategory === category || 
  product.articleType === category
);
export const getProductsByBrand = (brand) => products.filter(product => product.brand === brand);
export const getProductsByColor = (color) => products.filter(product => product.color === color);
